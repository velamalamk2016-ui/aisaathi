export function getTranslation(key: string, language: Language): string {
  const translations = TRANSLATIONS[key as keyof typeof TRANSLATIONS];
  if (!translations) return key;
  
  return translations[language] || translations.english || key;
}

export function getDynamicTranslation(text: string, language: Language): string {
  // Check if the text exists in dynamic content translations
  const dynamicContent = TRANSLATIONS['dynamic-content'] as any;
  if (dynamicContent && dynamicContent[text]) {
    return dynamicContent[text][language] || dynamicContent[text].hindi || text;
  }
  // Return original text if no translation found
  return text;
}

